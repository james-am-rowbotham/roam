function radiusVars(): [string, string][] {
  return Object.entries(radius).map(([k, v]) => [`--radius-${k}`, `${v}px`]);
}

/**
 * Render the shared colour + radius tokens as a Tailwind v4 `@theme` block.
 * Web imports the generated output (tokens.css) so its design variables come
 * from this one source — never re-typed. Fonts and the content-shell width stay
 * web-only (next/font binding) and live in the app's globals.css.
 */
export function themeCss(): string {
  const lines = [
    '/* GENERATED by @roam/tokens — do not edit. Run `bun run build` in packages/tokens. */',
    '@theme {',
    '  /* Colours */',
    ...colorVars().map(([k, v]) => `  ${k}: ${v};`),
    '',
    '  /* Radius scale */',
    ...radiusVars().map(([k, v]) => `  ${k}: ${v};`),
    '}',
    '',
  ];
  return lines.join('\n');
}
