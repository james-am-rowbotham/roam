import { colors } from './colors';
import { radius } from './scale';

// Flat CSS custom-property names (kebab) → token value. These names are the
// contract the web's Tailwind utilities depend on (e.g. --color-app → `bg-app`,
// --color-line → `border-line`), so keep them stable.
